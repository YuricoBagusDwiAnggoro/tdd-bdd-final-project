# uncomment for debugging failing tests
# logging.disable(logging.CRITICAL)

# DATABASE_URI = os.getenv('DATABASE_URI', 'sqlite:///../db/test.db')
DATABASE_URI = os.getenv(
    "DATABASE_URI", "postgresql://postgres:postgres@localhost:5432/postgres"
)
BASE_URL = "/products"


######################################################################
#  T E S T   C A S E S
######################################################################
# pylint: disable=too-many-public-methods
class TestProductRoutes(TestCase):
    """Product Service tests"""

    @classmethod
    def setUpClass(cls):
        """Run once before all tests"""
        app.config["TESTING"] = True
        app.config["DEBUG"] = False
        # Set up the test database
        app.config["SQLALCHEMY_DATABASE_URI"] = DATABASE_URI
        app.logger.setLevel(logging.CRITICAL)
        init_db(app)

    @classmethod
    def tearDownClass(cls):
        """Run once after all tests"""
        db.session.close()

    def setUp(self):
        """Runs before each test"""
        self.client = app.test_client()
        db.session.query(Product).delete()  # clean up the last tests
        db.session.commit()

    def tearDown(self):
        db.session.remove()

    ############################################################
    # Utility function to bulk create products
    ############################################################
    def _create_products(self, count: int = 1) -> list:
        """Factory method to create products in bulk"""
        products = []
        for _ in range(count):
            test_product = ProductFactory()
            response = self.client.post(BASE_URL, json=test_product.serialize())
            self.assertEqual(
                response.status_code, status.HTTP_201_CREATED, "Could not create test product"
            )
            new_product = response.get_json()
            test_product.id = new_product["id"]
            products.append(test_product)
        return products

    ############################################################
    #  T E S T   C A S E S
    ############################################################

    # Existing test cases...

    # ----------------------------------------------------------
    # TEST READ - GET PRODUCT
    # ----------------------------------------------------------
    def test_get_product(self):
        """It should Get a single Product"""
        # Create a product
        test_product = self._create_products(1)[0]
        
        # Make a GET request to retrieve the product
        response = self.client.get(f"/products/{test_product.id}")
        
        # Assert that the response status code is HTTP_200_OK
        self.assertEqual(response.status_code, 200)
        
        # Get the JSON data returned by the API
        data = response.get_json()
        
        # Assert that the product's name matches the expected value
        self.assertEqual(data["name"], test_product.name)
    
    def test_get_product_not_found(self):
        """It should not Get a Product that's not found"""
        # Make a GET request to a non-existent product ID
        response = self.client.get("/products/0")  # 0 is assumed to be an invalid ID
        
        # Assert that the response status code is HTTP_404_NOT_FOUND
        self.assertEqual(response.status_code, 404)
        
        # Check if the response JSON contains an appropriate message
        data = response.get_json()
        self.assertIn("was not found", data["message"])